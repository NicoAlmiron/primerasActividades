let numero1 = parseInt(prompt("Ingrese un numero"));

document.write("El numero ingresado es divisible por: ");

if (numero1 % 2 == 0) {
    document.write(" 2 ");
}

if (numero1 % 3 == 0) {
    document.write(" 3 ");
}

if (numero1 % 5 == 0) {
    document.write(" 5 ");
}

if (numero1 % 7 == 0) {
    document.write(" 7 ");
}